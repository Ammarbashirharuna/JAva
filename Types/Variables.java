/**
 * Declares the access modifier for a class, method, or variable.
 * The `public` access modifier allows the class, method, or variable to be accessed from anywhere in the program.
 */
public  class Variables {

    public static void main(String[] args) {
        int i = 0;
        int y = 10;
        int sum = i + y;
        System.out.println(sum);
        float f = 10.0f;
        double d = 10.0;
        boolean b = true;
        System.out.println(f);
        System.out.println(d);
        System.out.println(b);
    }
}